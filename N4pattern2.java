class N4pattern2
{



    public static void main(String[] args){

        int r=5;
        int n=1;

        for(int i=1;i<=r;i++)
        {
           

            for(int k=1;k<=n;k++)
            {

                System.out.print(n);

            }
            

            n++;
            System.out.println();


        }
    }
}
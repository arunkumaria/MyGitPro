class N4pattern11
{



    public static void main(String[] args){

        int r=5;
        int n=1;

        for(int i=1;i<=r;i++)
        {
           
            
            for(int k=n;k >= 1;k--)
            {

                System.out.print(k);

            }
            n++;
            
            System.out.println();


        }
    }
}
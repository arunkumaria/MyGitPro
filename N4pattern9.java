class N4pattern9
{



    public static void main(String[] args){

        int r=5;
        int n=5;

        for(int i=1;i<=r;i++)
        {
           

            for(int k=1;k<=n;k++)
            {

                System.out.print(k);

            }
            

            n--;
            System.out.println();


        }
    }
}
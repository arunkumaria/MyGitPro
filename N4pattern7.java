class N4pattern7
{



    public static void main(String[] args){

        int r=5;
        int n=1;

        for(int i=1;i<=r;i++)
        {
           

            for(int k=5;k>=n;k--)
            {

                System.out.print(k);

            }
            

            n++;
            System.out.println();


        }
    }
}
class Apattern27
{



    public static void main(String[] args){

        int r=5;
        int n=65;
       

        for(int i=0;i<r;i++)
        {

        

            for(int k=n;k<=(n+4);k++)
            {
               

                System.out.print((char)k);

            }
            n++;
            System.out.println();
            

        }
    }
}
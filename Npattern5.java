class Npattern5
{


    public static void main(String[] args){

        int r=7;
        int n=1;

        for(int i=1;i<=r;i++)
        {
           

            for(int k=7;k>=n;k--)
            {

                System.out.print(k);
            }
            System.out.println();
            n++;
           
            
         
        }
    }
}